/**
Problem:
You are given a sequence a1,a2,…,an consisting of n integers.
You may perform the following operation on this sequence: choose any element and either
increase or decrease it by one.
Calculate the minimum possible difference between the maximum element and the minimum
element in the sequence, if you can perform the aforementioned operation no more than k
times.
**/
ll n,k,ara[100005],sum[100005];
ll Valid(ll dif)
{
    ll tot=1e17;
    for(ll i=1 ; i<=n ; i++){

        ll cur1 = ara[i];       /// Assumed minimum value of the array
        ll cur2 = ara[i]+dif;   /// Assumed maximum value of the array

        ll pos2 = upper_bound(ara+1 , ara+n+1 , cur2)-ara;

        ll incrementCost = ((i-1)*ara[i]) - sum[i-1];
        /** Cost for make all the elements equal to cur1 those who r less than cur1 **/
        ll decrementCost = (sum[n]-sum[pos2-1]) - ((n-pos2+1)*cur2);
        /** Cost for make all the elements equal to cur2 those who r greater than cur2 **/

        tot = min(tot , incrementCost+decrementCost);
    }
    for(ll i=1 ; i<=n ; i++){

        ll cur1 = ara[i]-dif;
        ll cur2 = ara[i];

        ll pos1 = lower_bound(ara+1 , ara+n+1 , cur1)-ara;
        pos1--;

        ll incrementCost = (pos1*cur1) - sum[pos1];
        /** Cost for make all the elements equal to cur1 those who r less than cur1 **/
        ll decrementCost = (sum[n]-sum[i]) - ((n-i)*cur2);
        /** Cost for make all the elements equal to cur1 those who r greater than cur2 **/

        tot = min(tot , incrementCost+decrementCost);
    }
    return tot;
}
ll BiSec()
{
    /** Assume a difference & check it is valid or not? **/
    ll lo=0,hi=ara[n]-ara[1],mid,res=ara[n]-ara[1];
    while(lo <= hi){
        mid = (lo+hi)/2;
        if(Valid(mid) <= k){
            res = mid;
            hi = mid-1;
        }
        else
            lo = mid+1;
    }
    return res;
}
