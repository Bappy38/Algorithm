/**
Knapsack - 2
n<=100  ;   W<=1e9  ;   w[i]<=W     ;   v[i]<=1e3
Find the maximum possible sum of the values(Profit) of items that u can takes home.
Observation:
i.  Here we can gain maximum profit n*v[i] = 1e5.
ii. So,we'll try to minimize the weight as much as possible in opposition to value.
iii.That means here are two states.They r Position and Value. Total complexity 100*1e5=1e7.
**/
int w[105],val[105];
ll i,j,n,W,ans=0,temp;
ll dp[105][100005];
void KnapSack()
{
    dp[n+1][0]=0;
    for(j=1 ; j<=100002 ; j++)
        dp[n+1][j]=longlimit;
    for(i=n ; i>=1 ; i--)
    {
        for(j=0 ; j<=100000 ; j++)
        {
            dp[i][j] = dp[i+1][j];      /** If i don't take the ith
             item then the weight remains same as after taking previous item **/
            if(j >= val[i])
                dp[i][j] = min(dp[i][j] , w[i]+dp[i+1][j-val[i]]);  /** If I take ith
            item then I've to minimize the weight against value **/
        }
    }
    for(i=100000 ; i>=0 ; i--)
    {
        if(dp[1][i] <= W)   /** What is the maximum value if I've started from item 1 **/
        {
            cout<<i<<endl;
            break;
        }
    }
}
